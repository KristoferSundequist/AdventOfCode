long foo(int n, int age)
{
    if (memo.TryGetValue(new { N = n, Age = age }, out var gotIt))
    {
        return gotIt;
    }

    if (n == 0)
    {
        return 1;
    }

    var value = (age == 0)
        ? foo(n - 1, 6) + foo(n - 1, 8)
        : foo(n - 1, age - 1);

